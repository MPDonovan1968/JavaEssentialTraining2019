package com.company;

public class CalcHelper {

    public static double addValues(double d1, double d2 ) {
        System.out.println("in new helper class");
        return d1 + d2;

    }

    public static double subValues(double d1, double d2 ) {
        System.out.println("in new helper class");
        return d1 - d2;

    }

    public static double multValues(double d1, double d2 ) {
        System.out.println("in new helper class");
        return d1 * d2;

    }

    public static double divValues(double d1, double d2 ) {
        System.out.println("in new helper class");
        return d1 / d2;

    }

}
